    {
        $stock_balance =  Sales::stockBalance($request->supplier_id,$request->qty);
        if ($stock_balance) {
           flash('Sorry requested qty not available for the selected supplier','alert-success');
            return back();
        }
        $customer      = Sales::customerCreate($request);
        $payment       = Sales::paymentCreate($request);
        $payment_type  = Sales::paymentTypeCreate($request,$payment->id);

        flash('Sales created successfully','alert-success');
        return back();
